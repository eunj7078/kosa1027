
public class Ho27 {
	public static void main(String[] args) {
		int dan1 = 6;
		int dan2 = 7;
		int gom1 = 4;
		int gom2 = 6;
		for (int i = dan1 ; i <= dan2 ; i++) {
			for (int j  = gom1 ; j <= gom2; j++) {
				System.out.print( i + " * " + j + "= " + i*j + "           ");
			}
			System.out.println();
			
		}
	}

}
